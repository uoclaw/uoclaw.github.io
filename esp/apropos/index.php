	<h2>Acerca de JuriGlobe</h2>
	<p>JuriGlobe   es un   grupo de investigación formado por profesores de la Facultad de  derecho  de la Universidad de Ottawa  para  elaborar  y desarrollar un banco electrónico de datos,  plurilingüe, accesible en el Internet, sobre los sistemas jurídicos en el mundo, los idiomas oficiales que utilisan así
	  como  sobre un cierto numero de convenciones y otros instrumentos comerciales  multilaterales entre los mas importantes.</p>
	<p> Asi un  primer objetivo es  ofrecer a los  juristas y a la gente de negocios un instrumento simple y moderno que les  permiten una primera indicación sobre el sistema jurídico vigente y sobre los idiomas  oficiales utilizados en los varios países a los cuales  planean viajar o llevar sus negocios.</p>
	<p> Un  segundo objetivo es facilitar un instrumento pedagógico visual para ilustrar y  favorecer el desarrollo de la enseñanza del derecho comparado y del derecho  internacional en el contexto de la formación de los recursos humanos que necesita  la globalización de los intercambios.</p>
	<p> Un  tercer objetivo es fomentar discusiones sobre la buena gobernanza de dicha  globalización  con vistas a  la elaboración de instrumentos comerciales  multilaterales a la vez fiables y facilmente accesibles, y que  tengan en cuenta la diversidad de culturas jurídicas, de idiomas, de economías y de demografía.</p>
