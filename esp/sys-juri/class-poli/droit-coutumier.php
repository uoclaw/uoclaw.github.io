	<h2>Sistemas de derecho consuetudinario y sistemas  mixtos de derecho consuetudinario </h2>
	<p>Al presente existen muy pocas entidades políticas cuyos sistemas  puedan ser propiamente designados como enteramente consuetudinarios. La  costumbre puede tener aspectos muy diversos, según se radica en la sabiduría  elaborada por la experiencia cuotidiana, o, más intelectualmente, en las  grandes tradiciones espirituales o filosóficas. No obstante, el derecho  consuetudinario (en tanto como sistema y no solamente como supletorio del derecho  positivo) juega un rol de gran importancia, particularmente en materia de estatuto personal, en un número relativamente elevado  de países de derecho mixto. Esto es evidente en ciertos países africanos. Lo  mismo se puede decir, por ejemplo, en   condiciones por cierto diferentes, como el derecho de China o de India.</p>
