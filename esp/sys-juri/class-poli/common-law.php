	<h2>Sistemas de  Common law y sistemas mixtos con Common law</h2>
	<p>De la misma manera que el sistema de derecho civil, el sistema del  Common law opera en ambientes culturalmente diversos del mundo. A pesar de las  diferencias algunas veces considerables que genera esta diversidad, y que a  menudo son amplificadas por las circunstancias políticas, se pueden agrupar  dentro de esta categoría aquellos países en los cuales el derecho reposa  técnicamente, al menos en lo esencial, sobre los conceptos y los modos de  organización jurídica del common law británico, que concede un lugar primordial  a la jurisprudencia, y no a la ley como medio ordinario de expresión del  derecho común. En consecuencia, se han mantinido en esta categoría a países o  entidades políticas más o menos apegados a 1a tradición británica, en los  cuales, a pesar de poseer en abundancia códigos, leyes instrumentos normativos no  jurisprudenciales, la jurisprudencia del common law conserva su carácter de  derecho fundamental (por ejemplo, el Estado de California en los Estados Unidos de América). </p>
