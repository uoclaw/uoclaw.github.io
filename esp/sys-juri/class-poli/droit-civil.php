	<h2>Sistemas de  derecho civil y sistemas mixtos con derecho civil  </h2>
	<p>Se encontrarán  dentro de esta categoría aquellos países que, junto con otras fuentes, han sido  principalmente influenciados por su herencia jurídica romana y que, dando  preeminencia al derecho escrito, han resueltamente adoptado una codificación  sistemática de su derecho común. Por otro  lado se encontrar&aacute;n países, generalmente de derecho mixto, que sin haber recurrido  a la técnica de la ley codificada, han retenido hasta cierto grado suficientes  elementos de construcción jurídica romana, como justificaci&oacute;n escrita, que permiten  considerarlos como afiliados a la tradición civilista ( tal es por ejemplo el  caso de Escocia ). Además, están incluidos en esta categoría países en los  cuales a pesar de que la influencia romana no ha sido tan importante, sin  embargo su derecho, codificado o no, reposa en una concepción del rol de la ley  cercana a ésa de los países de tradición civilista &laquo;&nbsp;pura&nbsp;&raquo; (tal es el  caso, por ejemplo de los países de tradición escandinava los cuales ocupan,  dentro del seno de la &laquo;&nbsp;familia romano-germánica&nbsp;&raquo;, una posición  original).</p>
