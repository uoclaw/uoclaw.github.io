	<h2>Nota  introductiva</h2>
	<p>Al  crearse este sitio sobre los sistemas jurídicos del mundo, se han establecido  cinco categorías: derecho civil, common law, derecho consuetudinario, derecho  musulmán, derecho mixto. Esta última categoría no se refiere a un sistema, sino  a una combinación de sistemas. Esta categorización  y  la clasificación,  obviamente imperfecta, de las entidades políticas que de ella resulta,  necesitan algunas observaciones preliminarías.</p>
	<p> La  expresión “entidades políticas” incluye los Estados miembros de las Naciones Unidas,  así como los pocos territorios independientes  que no forman parte de aquellas, pero también en algunos casos divisiones  políticas de aquellos Estados. Por consecuencia, en el caso de ciertos de estos  territorios no independientes (que a veces pueden gozar de una autonomía  interna mas o menos fuerte) , pareció útil identificar sus sistemas jurídicos,  sea que por sus colocaciones geográficas lejanas el lazo con  el sistema del país de vinculación no es  evidente (tal es por ejemplo el  caso de los territorios franceses del Pacífico,  del Océano Índico o del Caribe) , sea que por ser parte de un conjunto político  de tipo federal u otro, su sistema a adquirido, o mantenido, en el marco de  aquellos conjuntos, características distintas (tal es el caso por ejemplo de Québec, entidad  de derecho mixto , cuyo derecho común  en  el marco de importantes campos, especialmente pero no exclusivamente  de derecho privado,  compete al derecho civil codificado, mientras  el derecho común en las otras provincias y territorios de Canadá es esencialmente basado en el common law). Mencionaremos que el hecho de tomar en  cuenta aquellos territorios no independientes ha tenido a veces por  consecuencia la obligación de desglosar algunos números de población nacional o  de aumentar la aparente importancia del numero de paises correspondientes a uno o al otro de los  sistemas. Los inconvenientes que podrían deducirse en cuanto a una buena  interpretación de los varios cuadros son reducidos por el hecho que es  claramente mencionado, después del nombre de aquellos territorios, provincias o  Estados, el nombre del Estado central al cual son vinculados.</p>
	<p> En lo  que se refiere a los derechos religiosos, solamente hemos tomado en cuenta 
	  el derecho musulmán, a razón de su permanencia  y de su gran extensión. El derecho judío es sin embargo señalado una vez  para tener en cuenta las particularidades del  sistema de derecho mixto del Estado de Israel. Otros sistemas originalmente  religiosos han perdido hoy día su carácter y su distinto estatuto por motivo de  una mas o menos integración de un numero mas o menos grande de sus elementos en  los sistemas consuetudinarios o estatales.</p>
	<p>Por otra parte, nos parece importante recordar que el derecho&nbsp;canónico no es un derecho religioso: aunque informado por dogmas religiosos, es un derecho de origen humano, que pertenece sin ninguna duda a la familia del derecho civil.</p>
	<p> Los derechos  consuetudinarios, cuya categoría incluye también  a los llamados derechos autóctonos, no han  sido señalados en los casos en los cuales, aunque se puedan a veces tomar en  cuenta a ciertas fines, no constituyen una caracteristica  sistémica del derecho de  la entidad política considerada.</p>
	<p> Pensándolo  bien,  hemos dejado de lado la categoría  de los derechos socialistas, hace poco inevitable en ciertas clasificaciones.  Es verdad que el marxismo-leninismo, a pesar de los recientes cambios  políticos, tiene todavía una cierta influencia a veces importante sobre la  organización jurídica de  algunos países.  Pero el criterio que había  presidido a  la creación de la categoría de los derechos socialistas, para oponerlos a los  derechos occidentales, era un criterio material, cuando, sin atenernos a un  criterio superficialmente formal, hemos mas bien privilegiado  en líneas generales los aspectos metodológicos  y técnicos de los sistemas, los conceptos jurídicos y los modos de elaboración  y de expresión del derecho.</p>
	<p> Por  otro lado, notaran que, en el capitulo titulado &laquo;&nbsp;Clasificación de los sistemas jurídicos y  entidades políticas correspondientes&nbsp;&raquo;,   las secciones presentan los sistemas ( de derecho civil, de common law, de derecho musulmán, de derecho consuetudinario y por supuesto mixtos) al plural y no al  singular. Quisimos así tener en cuenta el hecho de que cada sistema jurídico  ( como  categoría de clasificación ) tiende a adquirir características particulares  según los territorios y las poblaciones a los cuales se aplica. A este propósito  podemos recordar como ejemplos las diferencias que existen entre los derechos  positivos de los Estados-Unidos, del Reinado-Unido y de Australia ( países de tradición de common law ),  y no existen   por menos entre los derechos positivos de Francia, de Alemania y de  Chile ( países de tradición civilista ). </p>
	<p>Para  terminar, no nos parece inútil insistir de nuevo sobre los modestos objetivos  de información que hemos intentado. Por este motivo nos hemos limitado a  categorías fácilmente identificables por la comunidad jurídica mundial. Sin embargo y de ninguna manera se podra interpretar esta  presentación como una desaprobación de los esfuerzos mas científicos o mas  refinados de clasificación  ( los de Constantinesco, de Zweigert y K&ouml;tz, de  Timsit, de Glenn por ejemplo) , pero que  no hubieran correspondido mejor al objetivo inmediato  que nos proponíamos.  </p>
