	<h2>Nota introductiva</h2>
	<p>En este sitio la palabra  “idioma” se refiere solamente a los idiomas oficiales. Sin embargo “la  definición de lo que es un idioma oficial no es perfectamente clara” (Michel  Malherbe).</p>
	<p>Varios idiomas oficiales  lo son <em>de jure</em> en este sentido que  son expresamente reconocidos por la constitución o por la ley, bajo  denominaciones tales como “idioma oficial”, “idioma nacional”, “idioma del  Estado”, o “Idioma de la rep&uacute;blica”. Otras veces se deduce claramente de textos  que imponen un idioma o permiten su uso a varios fines institucionales.  Conviene mencionar que el carácter oficial de estos idiomas no impide que  puedan a veces tener lo que se puede llamar,   sin connotación peyorativa, idiomas a estatuto limitado, sea que su  reconocimiento se limite a ciertas partes del territorio, sea que se limite a  ciertos campos de actividades (frecuentemente a educación, protección cultural o  comunicaciones entre la administración y los ciudadanos) o sea por último que,  en la realidad, sea de naturaleza más bien simbólica, a pesar de su aparente  gran extensión. </p>
	<p>Varios idiomas oficiales  lo son, como se dice a veces, <em>de facto</em>,  en el sentido que su estatuto, sin reconocimiento por textos específicos,  resulta de su empleo constante, a veces secular, en todos los campos mayores de  actividad estatal (un ejemplo notable es el del inglés en los Estados Unidos de  América tanto al nivel federal que en una minoría importante de los Estados  federados). Pero existen también idiomas co-oficiales <em>de facto</em> a estatuto más o menos limitado.</p>
	<p>Teniendo en cuenta estos  cualitativos, y como se trata en este sitio de establecer una lista de los  idiomas oficiales en relación significativa con el sistema jurídico global de  cada entidad política, hemos retenido como idiomas oficiales (en total cerca de cien) solamente a los que se usan realmente de manera sustancial y  generalizada en la legislación y la administración de la justicia. Hemos  eliminado los idiomas oficiales a estatuto materialmente o territorialmente  limitados. Sin embargo la existencia, en una entidad política, de un o, a veces,  de varios idiomas oficiales a estatuto limitado esta indicada por el signo plus  (+).</p>
	<p>En la elaboración de  este sitio los autores se han fijado, en un importante n&uacute;mero de casos, sobre  su propio conocimiento de las entidades políticas consideradas, pero en un  n&uacute;mero de casos a&uacute;n mas importante, han tenido que referir a la análisis,  interpretación, y comprobación de varias fuentes. La evaluación a distancia, a través  de documentos y estudios tan cuidados sean, de las prácticas efectivas en  materia de idioma de las instituciones jurídicas, deja espacio para dudas y tal  vez para equivocaciones. Por este motivo comentarios, críticas, y sugestiones serán  bienvenidos en vista de asegurar la mejor exactitud de las informaciones  suministradas. </p>
