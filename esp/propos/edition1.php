	<h2>Prólogo de la primera edición  ( 1999 )</h2>
	<p>La idea de construir este sitio en la Web sobre los sistemas jurídicos del mundo se ha  desarrollado a partir de un interés especial en el tema. En efecto, tanto el derecho civil como  el common law se enseñan en la   Facultad de Derecho de la Universidad de Ottawa.  Además, gracias a <a href="http://www.droitcivil.uottawa.ca/fr/programmes-d-etudes/programme-national/le-programme-national/id-menu-291.html?pid=291">un programa nacional</a>, la Facultad ofrece a sus estudiantes la posibilidad  de obtener, en cuatro años, la doble formación jurídica en ambos sistemas, lo  cual favorece sus posibilidades laborales. Por consiguiente, ha parecido útil  ofrecerles, por medio de este sitio en la red, una perspectiva justa de la  importancia respectiva de estos dos sistemas en el mundo. </p>
	<p>Pero también un interés más general nos ha conducido a establecer este  sitio. En efecto, después  de 50 años de la liberación del comercio internacional bajo los efectos de  GATT-O.M.C., la baja de las tarifas aduaneras de 50% a 3% en los países  industrializados ha contribuido a multiplicar por trece el volumen del  intercambio comercial a escala mundial. En Canadá, por ejemplo, el 43% de las  actividades económicas están actualmente directamente relacionadas con las  exportaciones, en comparación al 18% de hace veinte años. Un empleo de cada  tres depende al presente de las exportaciones y se ha estimado que cada mil millón  de dólares de exportaciones nuevas crea de 6.000 a 8.000 empleos en el  país . </p>
	<p>En este nuevo contexto, nos ha parecido útil  proporcionar a juristas y gente de negocios un instrumento moderno que les  permitirá tener una primera indicación del sistema jurídico en vigor en los  diversos países donde es posible que ellos dirijan sus negocios o firmen  contratos commerciales internacionales. </p>
	<p>Ciertamente, la creación de esta herramienta presentó grandes desafios,  tales como la determinación de un criterio de clasificación de los diferentes  sistemas jurídicos dada la presencia de un gran número de sistemas mixtos, o  como saber cuál es exactamente el estado del derecho en países en plena  mutación, especialmente en Europa del Este, en Asia o en Africa. Sin embargo, nos ha sido imposible  pasar por alto la necesidad de este instrumento dentro de la mundialización. Es  por esto mismo que hemos decidido, plenamente conscientes de las dificultades y  por tanto con toda modestia, responder a este reto. Esperamos que los  resultados, aunque imperfectos, puedan responder a los objetivos previstos. Con  el tiempo, deseamos mejorar esta herramienta.. En consecuencia, hemos incluído  una página 'Foro de Discusión' con el objeto de recibir comentarios, críticas y  sugerencias del medio académico y de los usuarios en general. </p>
	<p>Este sito ha sido realizado en la Facultad de Derecho de la Universidad de Ottawa  por Graciela Fuentes (LL.M. McGill, LL.D. Ottawa) y por Nicola Mariani  (LL.L./LL.B. Ottawa), bajo la supervisión del profesor Alain-F. Bisson y del Decano Louis Perret. Queremos extender las gracias por su  ayuda y dedicación a Nathalie Lalonde de la Biblioteca de la Corte Suprema de  Canadá, a Marie-Claude Lavigne, especialista en computadoras, a Sylvie LeBlanc  y Serge Brousseau por sus apoyos técnico y a Diana-Lynn Swan de la Biblioteca de Derecho  de la Universidad  de Otttawa como así también a nuestros traductores Ken y Gail Larose (versión  inglesa) por su paciencia y rigor. </p>
