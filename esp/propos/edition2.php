	<h2>Prólogo de la segunda  edición  ( 2008 ) </h2>
	<p>La primera edición de  este sitio fue difundida en el <em>web</em> en  1999. Su principal objetivo era hacer una evaluación de la distribución  geográfica y demográfica de los sistemas jurídicos del mundo al final de siglo  XX y&nbsp;también de facilitar un instrumento de referencia a nuestros estudiantes del <a href="http://www.droitcivil.uottawa.ca/fr/programmes-d-etudes/programme-national/le-programme-national/id-menu-291.html?pid=291">programa de la doble  formación jurídica </a>en derecho civil y en common law.</p>
	<p>Teniendo en cuenta el  gran número de visitas que ha tenido este sitio así como de los comentarios y  sugestiones recibidos, nos pareció oportuno de emprender una nueva edición  revisada, aumentada y reestructurada.</p>
	<p>Así esta segunda edición  fue la ocasión de afinar la composición de los sistemas jurídicos de algunas de  las entidades políticas, particularmente de África y del Oriente, después de  reconsiderar sus características a la luz de nuevos elementos de información.  Naturalmente es inevitable que un sitio de tanta amplitud necesite correcciones  de vez en cuando, a pesar de todas las precauciones tomadas para asegurar su  exactitud gracias a la consulta de numerosas fuentes documentales y de personas  informadas. Pese a sus imperfecciones, los numerosos testimonios recibidos de  parte de los visitantes a este sitio nos han convencido de su utilidad.  Obviamente las sugestiones y críticas de parte de sus usuarios en vista de su  mejoramiento quedan siempre bienvenidas.</p>
	<p>Por otra parte la  originalidad de esta segunda edición queda en primer lugar en el añadido de una  parte sobre el peso económico respectivo de cada de los sistemas jurídicos  (PIB). Queda también en la puesta en relieve, por la elección de algunos  ejemplos significativos, de los esfuerzos desplegados por los Estados, las  organizaciones internacionales y la comunidad mercantil internacional en la  elaboración de un derecho mercantil internacional uniforme, en vista de evitar  a los problemas de conflictos trans-sistemicos de leyes y de jurisdicciones, a  fin de facilitar los intercambios transfronterizos en el contexto de la  globalización. Finalmente una última originalidad de esta nueva edición  consiste, en este mismo contexto, en indicar los idiomas oficiales de las  entidades políticas y en enseñar la distribución de aquellos en el mundo  contemporáneo según cada sistema jurídico.</p>
	<p>Esta segunda edición fue  realizada por Jabeur Fathally (D.E.S.D. Tunis; LL.M. Ottawa), profesor de tiempo  parcial de la Universidad de Ottawa, y por Nicola Mariani (LL.L./LL.B. Ottawa),  abogado internacional en Paris, bajo la supervisión del profesor emérito Louis Perret, ex-decano de la Facultad de derecho de  Ottawa (Sección de derecho civil), y del profesor emérito Alain-Francois  Bisson, de esta misma facultad. Las investigaciones estadísticas, los mapas  geográficos, y los gráficos fueron realizados por Ghislaine Pelletier (B.arch.  Montréal). Los graficos y los mapas fueron actualizados por Annie N’Tula Mankeni. La revisi&oacute;n de la bibliograf&iacute;a  fue hecha por Diana-Lynn Swan, y la de las estadísticas fue realizada por Sandrine Bidegorry. Las versiones en árabe, en ingl&eacute;s, en  español, en ruso y en chino son traducciones del sitio francés original. Las  tres primeras traducciones son la obra de los varios miembros de JuriGlobe,  mientras las en ruso y en chino corresponden respectivamente a Natalia Shpilkoskaya  ( LL.B. Moscú; LL.M. Ottawa)  y a Xiaodong  Liang ( LL.B. Chendung, LL.M. Laval ). La transposición de los ficheros en el  Web fue realizada por los webmestres de la Facultad de derecho: Maxime Brasseur y  Eric Proulx.  </p>
	<p>Esta segunda edición no  hubiera sido posible sin el apoyo y el aliento de Nathalie DesRosiers, decana  de la Facultad  de derecho (Sección de derecho civil), sin la financiación de la Universidad de Ottawa y de la Ontario Law Foundation y sin  la generosa contribución de Fatma El-Mehelmy, a quienes queremos extender las gracias.</p>
