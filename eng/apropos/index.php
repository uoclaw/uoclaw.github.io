	<h2>About JuriGlobe</h2>
	<p> JuriGlobe is a research group formed by professors from the Faculty of Law of the University of Ottawa, which focuses on the development of a multilingual information data bank, accessible to all on the internet, containing general information relating to the different legal systems in the world, to the different official languages and to some of the most important international commercial conventions, as well as other multilateral commercial tools.</p>
	<p> Consequently, a first objective is to provide lawyers and businessmen with a simple and modern device which enables them to have an initial overview of the applicable legal systems and the official languages which apply to the countries in which they are likely to operate, or where their endeavors may take them.</p>
	<p> A second objective of the project is to provide a visual educational tool aimed at illustrating and encouraging the teaching of comparative and international law, to train and prepare the students allowing them to perform in a globalized world.</p>
	<p> A third and final objective is to stimulate a discussion on the proper governance of globalization in order to develop dynamic multilateral international commercial tools which are both reliable and easily accessible and based on recognized principles and which take into account the diversity of the various legal systems, their languages and their economic and demographic importance. </p>
