	<h2>Introductory Remarks </h2>
	<p>Five categories  were selected for the creation of this Website on World Legal Systems: Civil  law, Common law, Customary law, Muslim law and Mixed law systems, the latter  referring not to a single system but to a combination of systems. This  categorization - and the admittedly imperfect classification of political  entities resulting there from - calls for a few preliminary remarks. </p>
	<p> The term &quot;political entities&quot;  encompasses member States of the United Nations, as well as the few independent  territories which are not part of it. It can also refer to political  subdivisions of countries as well. Indeed, it seemed important to identify the  legal systems of a number of non-independent territories (some of which even  enjoy varying degrees of autonomy), either because their geographic location  obscures their connection to the legal systems of distant countries (e.g. the  French territories in the Pacific Ocean, Indian Ocean or West Indies) or  because they belong to a federal or other political structure, yet their legal  system has acquired or maintained distinct characteristics within such  structure (e.g. Quebec, which is a mixed law jurisdiction whose general law, at  least in important areas pertaining particularly but not exclusively to private  law, is essentially a product of codified Civil law while, in the rest of  Canada, general law is essentially based on the Common law). It should be noted  that the taking into account of these non-independent territories separately  has sometimes created an obligation to break down some of the national  population figures or to increase the apparent importance of the number of  countries related to a given system. The drawbacks which  could arise and prevent a correct interpretation of the different charts are  diminished by the fact that clear reference to such territories, provinces or  States is followed by the name of the State to which they are attached. </p>
	<p>As for religious law systems, we have  included only Muslim law because of its permanent, broadly-based nature.  However we referred once to Jewish law to take into account the particularities  of Israel's  mixed legal system. Furthermore, some initially religious law systems have  since lost their character and distinct status due to the fact that a number of  their components have more or less been absorbed into customary or other legal  systems.</p>
	<p>Also, it must be pointed out that canon law is not a religious law system&nbsp;: it is a law inspired by religious dogma, but it is human in origin and belongs unquestionably to the Civil law family.</p>
	<p>Customary law, which category also  includes what is known as “Aboriginal laws” have not been highlighted in cases  where, even though they may be used for certain means, they do not represent a  key characteristic of the relevant political entity's legal system. </p>
	<p>Upon reflection, we have ruled out the  category of &quot;Socialist law&quot; whose inclusion, in the past, was  unavoidable in certain classifications. True, despite recent political  upheavals, Marxist-Leninist thought still plays a sometimes significant role in  the legal organization of certain countries. But the criterion which governed  the creation of a category of &quot;Socialist law&quot;, as opposed to western  law, was a material one, whereas on the whole we have given greater importance  to the methodological and technical aspects of the legal systems, to legal  concepts and to methods of developing and expressing law, without confining  ourselves to superficially formal criteria. </p>
	<p>Furthermore, you will note that the  headings in the chapter entitled &quot;Classification of Legal Systems and  Corresponding Political Entities&quot; refer to the legal systems in the plural  rather than in the singular form (e.g. Civil law systems, Common law systems,  Muslim law systems, Customary law systems and of course mixed law systems). Thus we wanted to account  for the fact that each legal system (as a classification category) tends to  acquire particular characteristics according to the territories and populations  it serves. Need we be reminded that, despite their affiliation to the same  legal family, there are significant differences between the positive laws of  the United States, the United Kingdom and Australia, for example (Common law  countries) and that the differences between the positive laws of France,  Germany and Chile, for example (Civil law countries) are no less substantial! </p>
	<p> In concluding it may be wise once again  to emphasize the very modest objectives we have set ourselves with this  information. Thus we have confined ourselves to categories which are easily  identifiable for the international legal community. However, our attempts should not  be construed as a rejection of classification efforts (e.g. those of  Constantinesco, Zweigert and Koltz, Timsit and Glenn) which, though more  scientific or more refined, would not have served our immediate purposes any  better. </p>
