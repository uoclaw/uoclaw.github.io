	<h2>Civil law Systems and Mixed Systems with a Civil law Tradition </h2>
	<p>	  In this category you will find political  entities that, apart from other sources, have drawn their inspiration largely  from the Roman law heritage and which, by giving precedence to written law,  have resolutely opted for a systematic codification of their general law.  However, you will also find political entities, generally with mixed legal  systems, which may not have resorted to the technique of codification but which  have retained, to varying degrees, a sufficient number of elements of Roman  law as &quot;Raison écrite&quot; to warrant their affiliation to the civilian tradition  (e.g. Scotland). On the other hand, this category also includes political  entities less influenced by Roman law but whose law, whether codified or not,  is founded on a perception of the role of statute law which, in many regards,  approaches that of countries with a &quot;pure&quot; civilian tradition (e.g.  Scandinavian countries, which occupy an original position in the Civil law, or  sometimes called &quot;Romano-Germanic&quot;, family). </p>
