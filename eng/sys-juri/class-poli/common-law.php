	<h2><strong>COMMON LAW SYSTEMS AND MIXED SYSTEMS WITH A  COMMON LAW TRADITION</strong></h2>
	<p> Like that of Civil law, the Common law  system has taken on a variety of cultural forms throughout the world.  Notwithstanding the significant nuances that such diversity can sometimes  create, and which political circumstances further accentuate, this category  includes political entities whose law, for the most part, is technically based  on English Common law concepts and legal organizational methods which assign a  pre-eminent position to case-law, as opposed to legislation, as the ordinary  means of expression of general law. Thus this category includes political  entities that  have more or less close ties with the English tradition and  that sometimes possess an abundance of codes, legislation and  non-jurisprudential normative instruments, but for which Common law  jurisprudence retains its character as the fundamental law (e.g. California in the United States).</p>
