	<h2>CUSTOMARY LAW SYSTEMS AND MIXED SYSTEMS  WITH A CUSTOMARY LAW TRADITION</h2>
	<p>	  Today, hardly any political entity in  the world operates under a legal system which could be said to be typically and  wholly customary. Custom can take on many guises, depending on whether it is  rooted in wisdom born of concrete daily experience or more intellectually based  on great spiritual or philosophical traditions. Be that as it may, customary  law (as a system, not merely as an accessory to positive law) still plays a  sometimes significant role, namely in matters of personal status, in a  relatively high number of political entities with mixed legal systems. This  obviously applies to a number of African countries but is also the case, albeit  under very different circumstances, as regards the law of China or India, for example. </p>
