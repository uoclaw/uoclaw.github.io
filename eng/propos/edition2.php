	<h2> Foreword of the 2nd edition (2008) </h2>
	<p>The first edition of this site was published on the web in 1999. The goal was then mainly to establish an inventory regarding the territorial and demographic distribution of the legal systems throughout the world at the end of the XX<sup>th</sup> century as well as to provide a reference tool for our students in the context of our double training program in civil law and common law (<a href="http://www.droitcivil.uottawa.ca/index.php?option=com_content&Itemid=291&id=1313&lang=fr&task=view&pid=291&lang=en">National Program</a>).</p>
	<p> Taking into account the growing popularity of the website, as well as the input received over the years, it appeared necessary to undertake a revised edition of the website.  We also took this opportunity to add information, re-examine, increase and restructure the website.</p>
	<p> Thus this second edition allowed us to review and reassess the composition of certain political entities' legal systems, particularly those of African and of the Middle Eastern countries, after reconsideration of their characteristics in light of further research. It is indeed unavoidable that a site of such wide scope needs to be reviewed from time to time, in spite of all the precautions taken to ensure its exactitude.  This was made possible by having access to many recent documentary sources and with help of well informed contributors. In spite of the imperfections of the website, many of the testimonies received from visitors of our website convinced us of its utility. It goes without saying that the users' comments and suggestions which aim to improve the site are welcome.</p>
	<p> The originality of this second edition lies in the addition of a section related to the respective economic weight of each legal system (GDP). It also resides in the choice of some significant examples, in highlighting the efforts made by the States, the international organizations and the international commercial community to develop a uniform international commercial law, in order to avoid trans-systemic issues of conflicts of laws and jurisdictions and to facilitate trans-border exchanges in the context of globalization. A last originality of this new edition finally consists, in this same context, in indicating the official languages of the political entities and to show their distribution in the contemporary world according to each legal system.</p>
	<p> This second edition has been carried out by Mr. Jabeur Fathally (D.E.S.D. Tunis; LL.L., LL.M., Ph.D. Ottawa), Assistant Professor at the University of Ottawa and by Mr. Nicola Mariani (LL.L/LL.B. Ottawa), International Lawyer, under the direction of Emeritus Professor Louis Perret, former Dean of the Faculty of Law of the University of Ottawa (Civil law section) and of Emeritus Professor Alain-François Bisson, of the same Faculty. Statistical research, maps and graphs were carried out by Ms. Ghislaine Pelletier (B.Arch. Montreal). The graphs and the maps were actualized by Ms. Annie N’Tula Mankeni. Ms. Sandrine Bidegorry did the statistics proof-readings. The bibliography was edited by Ms. Diana-Lynn Swan. The Arabic, English, Spanish, Chinese and Russian versions  are translations of the French original Website. The first three translations are the result of a combined effort of various members of the JuriGlobe team. Those in Chinese and Russian are respectively due to Mr. Xiaodong Liang ( LL.B.Chendung; LL.M. Laval ) and Ms. Natalia Shpilkovskaya ( LL.B. Moscow; LL.M. Ottawa ). The transposition of the files on the web has been achieved by the Webmasters of the Faculty of Law of the University of Ottawa, Mr. Maxime Brasseur and Mr. Eric Proulx.</p>
