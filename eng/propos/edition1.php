	<h2> Foreword of the 1st edition (1999) </h2>
	<p>The initial idea to create this  Website on &quot;World Legal Systems&quot; stems from a particular interest:  the fact that both Civil law and Common law are taught at the Faculty of Law of  the University of   Ottawa. The Faculty,  through its <a href="index.php?option=com_content&Itemid=291&id=1313&lang=fr&task=view&pid=291&lang=en">National Program</a>, further offers its students the possibility of  taking a degree in both legal systems within a four-year time period, thereby  improving their employment prospects. Thus it seemed desirable to afford them,  through this Website, a true perspective of the importance of each of these two  legal systems on an international level. </p>
	<p>However,  in setting up this Website, we were also guided by an interest of a more  general nature. Indeed, after 50 years of world trade liberalization brought  about through GATT and the WTO, tariff reductions - from 50% to 3% in  industrialized countries - have been instrumental in achieving a thirteen-fold  increase in the volume of trade worldwide. In Canada, for example, 43% of  economic activity today is directly related to exports, compared to only 18%  twenty years ago. One job in three now depends on exports and it is estimated  that, for each billion dollars in new exports, 6,000 to 8,000 jobs are created  in this country. </p>
	<p>In  the light of this new context, we thought it would be helpful to provide the  legal community and business people with a modern tool that would give them an  idea of the legal system in force in the various countries in which they might  be conducting business and entering into international commercial agreements. </p>
	<p>Certainly  the creation of such a tool posed quite a challenge, such as determining the  criteria for classifying the different legal systems when faced with a large  number of mixed legal systems, or ascertaining the status of the law in countries  undergoing a complete transformation, particularly in Eastern Europe, Asia or Africa. However, it is becoming  increasingly difficult to function without such a guide in the context of  globalization. Thus, fully aware of the pitfalls and hence in all modesty, we  nevertheless decided to take up the challenge. We hope the results, imperfect  though they may be, have achieved the intended purpose. Since we expect to  improve this instrument over time, we have included a &quot;Discussion  Forum&quot; page on our Website in order to receive comments, criticism and  suggestions from the academic world and users at large. </p>
	<p>This  Website was created at the Law Faculty of the University  of Ottawa by Mr. Nicola Mariani  (LL.L./LL.B. Ottawa) and Ms. Graciela Fuentes (LL.M. McGill, LL.D. Ottawa) under the guidance  of Professor Alain-F. Bisson and Dean Louis Perret.  We would like to express our gratitude for their assistance and dedication to  Ms. Nathalie Lalonde of the Supreme Court of Canada Library, Ms. Marie-Claude  Lavigne, computer consultant, Ms. Sylvie Leblanc and Mr. Serge Brousseau for  their technical support, and Ms. Diana-Lynn Swan of the University of Ottawa  Law Library, not to forget our translators Mr. Ken Larose and Ms. Gail Larose,  whose patience and thoroughness we hereby acknowledge. </p>
