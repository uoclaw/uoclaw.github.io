	<h2> INTRODUCTORY REMARKS</h2>
	<p> In this website, &quot;languages&quot; means the official languages of the different political entities. However, &quot;the definition of what an official language stands for is not perfectly clear&quot; (Michel Malherbe).</p>
	<p> Some languages are &quot;official&quot; <em>de jure</em>, in such a sense that they are the subject of an express, constitutional or legislative recognition, under an appellation which may vary (generally referred to as &quot;official language&quot;, but also sometimes referred to as &quot;national language&quot;, &quot;language of the State&quot;, &quot;language of the Republic&quot;, etc.) or of a recognition which results from unambiguous texts imposing or allowing their use for various institutional means. It is important to note that the official nature of these languages does not prevent them from being sometimes known, without any pejorative connotation, as languages with restricted status, either that their recognition is limited to some parts of the territory or that it is limited to certain spheres of activities (usually, education, protection of a culture or for the use of official communications between an administration and its citizens), or finally that, in fact, it is of a rather symbolic nature in spite of its apparently great scope.</p>
	<p> It is sometime said that some languages are official <em>de facto</em>, in the sense that their status, which is not recognized in specific texts, results from their constant and sometimes secular use in all the major fields of the official activity of a state (a notable example is the use of English in the United States of America, as well at the federal level as in an important minority of the federate States). But there also exists <em>de facto</em> co-official languages that have a more or less restricted status.</p>
	<p> Based on the qualifications set out above, and since this website attempts to draw up the list of the official languages significantly related to the <em>global legal system</em> of each political entity, we have only retained as official languages (about a hundred) those which we believe are really substantially and generally used in both the legislation and  administration of the justice. We have thus discarded official languages with materially or territorially restricted status. However, the existence, in a political entity, of one or, sometimes, several official languages with restricted status is announced by a plus ( + ) sign.</p>
	<p> In developing this website, the authors have, in a significant number of cases, based themselves on their personal knowledge of the political entities considered, but they also had to rely, in an even more significant number of cases, on the analysis, the interpretation and the review of various sources. The appreciation is thus being made remotely and upon interposed documentations and studies. As reliable as these may be, of the real practices regarding languages of legal institutions, there are always doubts that may arise. This is why we will welcome any observation allowing us to ensure the exactitude of the information provided in this website. </p>
