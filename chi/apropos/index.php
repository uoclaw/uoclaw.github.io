	  <h2>全球法系简介 <br>
	    （JuriGlobe） </h2>
	  <p>全球法系是由渥太华大学法学院的教授组建的一个学术研究团体。该团体的组建旨在建立一个有关世界法律体系、多边贸易公约和其它条约、及其所使用的官方语言的多语种的、互联网化的信息库。 </p>
	<p>建立此信息库的首要目的是希望为法学学者及商界人士提供一个简单而现代化的途径，帮助他们形成对可能涉及的有关国家现行法律体系及其所使用的正式语种的初步认识，或者指导他们进行相关的贸易活动。 </p>
	<p>其次，在贸易全球化所必需的人文资源教育背景下，建立此信息库可以为明晰比较法和国际法教学，并促进其发展提供一个直观的教学工具。 </p>
	<p>另外，鉴于各国法律文化、语种以及经济、人口重要性的多样化，此信息库的建立也可激发人们对如何更好的控制全球化以便从法律安全和可行性的角度制订多边贸易协议的思考。 </p>
