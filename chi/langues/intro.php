	<h2>序言 </h2>
	<p>就“语言”而论，我们注意到本网站中的均为存在于各个政治实体内的官方语言。但是，“对什么是官方语言的界定却并非是完全清晰而明确的。” （米歇尔·马勒伯） </p>
	<p>某些官方语言是法律意义上的官方语言，就是说，它们被视为在宪法上和立法上均被认可的表达形式而被冠与不同的名称（诸如通常的官方语言、有时也被称为民族语言、国家语言或者共和国语言等等），或者被认为是由大量清晰的文字演绎而来的，被获准使用于各种公共目的表达形式。需要注意的是，这些语言的官方性质并不能排除它们有时被认为处于一种约束性状态，或者对它们的认可被限定在某些地域范围或某些活动范围之内（比如教育、文化保护或者被用于行政与国民之间的正式交流），实际上，纵然这些语言有着广泛的使用范围，但最终更加被注重的却是它们的象征意义。 </p>
	<p>有时某些官方语言被认为是实际意义上的官方语言，就是说即使它们的地位并没有被某些特定的文件所认可，但是这些语言却长期的始终的被使用于一国的各种官方的正式活动中（一个显著的例子就是英语在美国的使用，无论是从联邦一级还是各州内的主要少数民族均使用英语）。此外，也可能有实际意义上的、或多或少处于约束性状态的、并存的官方语言。 </p>
	<p>基于以上的定性，加之本网站尝试列出一份与各政治实体的世界法系密切相关的官方语言列表，我们仅列举了那些我们认为真正意义上被实际上和广泛的应用于一国的立法和司法行政管理当中的官方语言（共计百来个）。那些在现实上和地域上处于限制状态的语言不在此列。在一个政治实体中，可能存在一种或者有时多个处于限制状态的官方语言，我们用加号（+）将它们标示出来。 </p>
