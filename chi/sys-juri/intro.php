	<h2>前言 </h2>
	<p>这个介绍世界范围内不同法律体系的网站的制作过程涉及到了民法、普通法、习惯法、穆斯林法和混合法五个不同的法律类别，其中混合法并非自成体系而应被视为一种不同法律体系的结合。也许如此的一个对各个政治实体法律体系的划分并不尽完美，在此权且作抛砖引玉之用。</p>
	<p>所谓“政治实体”包括联合国的各成员国及其他某些相对独立的，甚至是有政治争议的少数区域。所以，在本网站的部分内容中出现的对一些非独立地区（例如一国之内的某自治区域）法律体系的认定是有意义的。这样的地区或许因为其所处地理位置的原因而导致其法律体系和距其遥远的所属国的法律体系之间的联系并不明显，比如法国在太平洋、印度洋和安的列斯海的海外属地；或许是某一联邦体制的组成部分，但在联邦体制内被获准或者是保留了不同于其他地区的特殊法律体系，比如属混合法体系的政治实体魁北克，即使加拿大其他省份的公共法律都建立在普通法体系基础之上，也尽管普通法出现在魁北克的许多重要方面，但却并不排斥作为民法体系之延续的私法的存在。对于类似的非独立地区的关注促使我们有时需要对该特定民族群体的数据进行分别评估或者突出其作为具有不同法律体系区域的重要性。通过不同图表对如此的一些地区、省份或者联邦成员国与其所依附的国家法律体系的清晰区别标示势必会弥补因笼统标注造成的欠缺。</p>
	<p>关于宗教法律，考虑到其适用的连续性和广泛性特征，我们仅研究了穆斯林法。在介绍以色列混合法系特殊性的时候，犹太法也曾被提及。至于其他的原始宗教法律，由于或多或少的掺杂了习惯的或者国家的因素而丧失了其原有的特征。</p>
	<p> 在某些例子里，涵盖了被称之为原著民法的习惯法即使基于某种考虑而被涉及到，但是由于它们并不足以代表某政治实体法律体系的系统特征而没有被提及。</p>
	<p>经过反复斟酌，我们放弃了在本站载入社会主义法，尽管在不少类别划分中它都是不能回避的一类法系。纵然近年来在某些国家出现过一些政治变革，但不容置辩的是，马克思列宁主义对这些国家的司法体系仍具有重要影响。但是，曾经为对抗西方法律体系而创建的社会主义法律体系的主导准则曾是一个非规范化的准则，因此，我们没有支持如此一个看似明确的准则，而是优先考虑了有关方法论和技术层面的观点、有关法律概念以及法律的制定和表达模式。</p>
	<p>值得注意的是，在名为“政治实体和法律体系对应分类”一章里，代表法律体系（民法系、普通法系、穆斯林法系、习惯法系和混合法系）的标题是复数而非单数。我们因而根据其所适用的不同地区和群体，将每种法律体系与其特性相对应。并且，即使某些国家的法律貌似同属一个体系却同样存在差异，有时这种差异甚至是显著的，比如同属普通法传统国家的美国、英国和澳大利亚的人为法间的差异；另如同属民法传统国家的法国、德国和智利的人为法间的区别。 </p>
	<p>坚持我们所探寻的谦逊求是的学术宗旨不无裨益。正因为如此我们才采用了被国际法学界所广为接受的分类法。我们在工作中并非排斥更加科学和细致的分类方法（诸如孔斯丹蒂内斯科、茨威格特和克茨、蒂姆斯特、格雷恩的观点），但唯恐过尤不及，达到我们的直接目的是最重要的。</p>
