	<h2><strong>&nbsp;</strong>单一民法系和具有民法系传统的混合法系 </h2>
	<p>被归为此类的政治实体的法律体系相比之下广泛的受到罗马法的影响，以成文法的法律形式将其普通法律编纂为体系化的法典。同样被归为此类的一些混合法律体系的政治实体，其法律虽不尽然运用法典化的方式，但仍然不同程度的保留了罗马法架构中的成文法因素，因此也被认为是民法系传统的一种传承，比如苏格兰。另外，被划归为本类的一些政治实体的法律体系虽然受罗马法的影响不大，但不论其法律体系是否采用法典化的形式，因为其立法观念接近，所以也被划归为纯民法系传统区域，有时也被称为原始状态下“罗马-日耳曼”法系的斯堪的纳维亚传统国家即属此例。<strong> </strong></p>
