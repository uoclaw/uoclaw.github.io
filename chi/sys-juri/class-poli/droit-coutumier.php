	<h2>单一制习惯法系和具有习惯法系传统的混合法系 </h2>
	<p>目前能被称之为纯粹的和完全的习惯法系政治实体已不多见。习惯法因溶合于具体的日常生活或者置根于哲学和思维的传统中，其表现形式已日趋多样化。但是，习惯法（作为一种法律体系而并非仅仅作为人为法的一种次要的补充）在相对数量众多的混合法系政治实体内，仍然具有相当的重要性，尤其在涉及到某人的身份地位时。一些非洲国家的法律体系就是显著的例子。当然，不尽相同，中国和印度的法律体系也同属此类。 </p>
