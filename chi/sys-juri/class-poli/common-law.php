	<h2>单一普通法系和具有普通法系传统的混合法系 </h2>
	<p>如同民法系一样，普通法系亦在各国以不同的文化模式出现。尽管细微或显著的差别导致了这种多样性，而且不同的政治形势也可能造成这种多样性的增加，被划归为此类的政治实体的法律体系均主要建立在英式的普通法概念及法律结构形式之上，即作为普通法律的通常表现形式，判例被优先遵从而非成文法典。因此，被归为此类的政治实体或多或少的都传承了英国传统，纵然有时拥有大量非判例的法典、法律以及其他的法规，其法律的本质仍保持了普通判例法的特征，例如美国加利福尼亚州的法律即是如此。 </p>
