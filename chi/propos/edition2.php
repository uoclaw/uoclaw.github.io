	<p>借此机会，基于对新的信息和认识的考虑，我们对某些，尤其是非洲和中东地区的政治实体的法律体系构成进行了调整。实际上，尽管为了保证内容的准确性我们参阅了大量的文献材料并详细咨询了有关学者，对一个涉及范围如此广泛的网站的内容进行适时的校正是无法避免的。虽然仍有不足之处，我们所收到的大量信息表明，本网站的作用获得了广大读者的首肯。我们一如既往的欢迎读者们对我们网站内容的改进提出宝贵的批评、意见和建议。 </p>
	<p>新版的新颖之处在于对各个法律体系中所包含的经济成分（国民生产总值）的附加考虑。这方面的变化通过几个显著的例子，以及通过不同的国家、国际组织和国际商业团体为制订统一的国际贸易法、为避免跨司法体系和管辖权所导致的冲突和为全球化背景下跨国贸易的便利化所付出的努力突显出来。新版的新颖之处还体现在标明了当前世界上具有不同法律体系的各个政治实体所使用的官方语言和分布情况。 </p>
