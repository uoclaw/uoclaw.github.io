	<p>由于渥太华大学法学院集民法系和普通法系两大不同法系的教学于一身，使我们产生了建立一个关于“世界法律体系”的网站的最初构想。不仅如此，得益于一项国家项目，渥太华大学法学院为本院学生提供了在四年学制内接受双重法系教学的可能，并帮助他们开创更加广阔的前程。因此，通过本网站帮助学生形成对世界范围内两大不同法系各自重要性的准确认识是有积极意义的。</p>
