	<h2 align="right" dir="RTL">أنظمة القانون العرفي &nbsp;و الأنظمة  المختلطة ذات قانون عرفي </h2>
	<p align="right" dir="RTL">إن القول بوجود کیان سیاسي یطبق القانون العرفي بصفة صرفة و بالمعنی الحرفی  لکلمة &quot; العرف&quot; فیه الکثیرمن المجازفة. فکلمة &quot;العرف&quot; تحتمل  معانی عدة : فقد تکون نابعة من حکمة و ممارسة الأسلاف أو أن تکون ، و بشکل أکثر  عمقا، قائمة علی مبادئ و تقالید فلسفیة أوروحیة علیا. لکن هذا لا یمنعنا من القول  بأن القانون الفرعي لایزال یلعب دورا هاما خاصة في میادین الأحوال الشخصیة و خاصة  داخل الکیانات السیاسیة ذات الأنظمة القانونیة المختلطة.</p>
