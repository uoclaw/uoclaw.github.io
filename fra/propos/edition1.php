	<h2> Avant-propos de la 1<sup>re</sup> édition (1999)</h2>
	<p> L'idée première de construire ce site sur  les systèmes juridiques dans le monde  est venue d'un intérêt particulier. En effet, le droit civil et la common law sont enseignés à la Faculté de droit de l'Université d'Ottawa. De plus, grâce à <a href="http://www.droitcivil.uottawa.ca/fr/programmes-d-etudes/programme-national/le-programme-national/id-menu-291.html?pid=291">un programme national</a>, la Faculté offre à ses étudiants la possibilité d'obtenir, en quatre années, la double formation juridique dans les deux systèmes, ce qui est susceptible de leur ouvrir des débouchés accrus. Il a donc paru utile de leur fournir, au moyen de ce site, une juste perspective de l'importance respective de ces deux systèmes dans le monde. </p>
	<p> Mais un intérêt plus général nous a conduit aussi à l'établissement de ce site. En effet, après 50 ans de libéralisation du commerce international sous l'effet du GATT et de l'O.M.C., la baisse des tarifs douaniers, de 50% à 3% dans les pays industrialisés, a contribué à multiplier par treize le volume des échanges commerciaux à l'échelle mondiale. Au Canada, par exemple, 43% des activités économiques sont maintenant directement reliées aux exportations, comparativement à seulement 18% il y a vingt ans. Un emploi sur trois dépend aujourd'hui des exportations et l'on estime que chaque milliard de dollars d'exportations nouvelles crée de 6 000 à 8 000 emplois au pays. </p>
	<p> Dans ce contexte nouveau, il nous a donc paru utile de fournir aux juristes et gens d'affaires un instrument moderne leur permettant d'avoir une première indication du système juridique en vigueur dans les divers pays où ils sont susceptibles de mener leurs entreprises et de conclure des contrats commerciaux internationaux. </p>
	<p> Certes, la création d'un tel outil posait de grands défis, tels que la détermination des critères de classification des différents systèmes juridiques face à la présence d'un grand nombre de systèmes mixtes, ou encore quant à la connaissance exacte de l'état du droit dans des pays en pleine mutation, notamment en Europe de l'Est, en Asie, ou en Afrique. Il est cependant devenu difficile de se passer d'un tel outil dans le contexte de la mondialisation. C'est pourquoi nous avons quand même décidé, en pleine conscience des difficultés et par conséquent en toute modestie, de relever ces défis. Nous espérons que les résultats, aussi imparfaits soient-ils, puissent répondre aux objectifs visés. Avec le temps, nous souhaitons par ailleurs améliorer cet instrument. En conséquence, nous avons inclus une page &quot; Forum de discussion &quot; sur notre site web en vue de recevoir les commentaires, critiques et suggestions du milieu universitaire et des usagers en général. </p>
	<p> Ce site a été réalisé à la Faculté de droit de l'Université d'Ottawa par Me Nicola Mariani (LL.L. / LL.B. Ottawa) et Madame Graciela Fuentes (LL.M. McGill, LL.D. Ottawa), sous la supervision du professeur Alain-F. Bisson et du doyen Louis Perret. Nous adressons également nos remerciements pour leur aide et dévouement à Mme Nathalie Lalonde de la Bibliothèque de la Cour suprême du Canada, à Mme Marie-Claude Lavigne, consultante en informatique, à Mme Sylvie LeBlanc et M. Serge Brousseau pour leur support technique et à Mme Diana-Lynn Swan de la bibliothèque de droit de l'Université d'Ottawa, sans oublier nos traducteurs M. Ken Larose et Mme Gail Larose pour leur patience et leur rigueur. </p>
	Enfin des remerciements tout particuliers vont à M. Hacen Ould Lebatt, ancien doyen de la Faculté de droit de l'Université de Nouaktchott et ancien recteur de cette université, dont l'aide a été précieuse pour la classification des systèmes d'Afrique et du Moyen-Orient, ainsi qu'à Mme Josée Tremblay, pour les renseignements concernant certains pays d'Asie du Sud-Est. Il va de soi cependant que nous demeurons éventuellement responsables de toute erreur d'interprétation des indications qu'ils nous ont fournies.</p>
