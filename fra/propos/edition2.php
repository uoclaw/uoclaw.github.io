	<h2>Avant-propos de la 2<sup>e</sup> édition (2008)</h2>
	<p>La première édition de ce site a été diffusée sur la toile en 1999. Son objectif était alors principalement d’établir un état des lieux concernant la répartition territoriale et démographique des systèmes juridiques dans le monde à la fin du XX<sup>e</sup> siècle, ainsi que de fournir un outil de référence efficace à nos étudiants dans le cadre de notre programme de double formation juridique en droit civil et en common law (<a href="http://www.droitcivil.uottawa.ca/fr/programmes-d-etudes/programme-national/le-programme-national/id-menu-291.html?pid=291">programme national</a> ).</p>
	<p>Compte tenu du grand nombre de visites dont ce site a été l’objet, ainsi que des suggestions et commentaires reçus, il nous est apparu opportun d’entreprendre une nouvelle édition, revue, augmentée et restructurée.</p>
	<p>Ainsi cette deuxième édition a été l’occasion de modifier la composition des systèmes juridiques de certaines entités politiques, notamment d’Afrique et d’Orient, après reconsidération de leurs caractéristiques à la lumière de nouveaux éléments d’information. Il est en effet inévitable qu’un site d’une telle ampleur appelle de temps à autre des corrections, en dépit de toutes les précautions prises pour en assurer l’exactitude grâce à la consultation de sources documentaires nombreuses et de personnes renseignées. Malgré ses imperfections, les nombreux témoignages reçus des visiteurs de ce site nous ont cependant convaincus de son utilité. Il va de soi au demeurant que les commentaires, suggestions et critiques des utilisateurs en vue de son amélioration demeurent toujours les bienvenus.</p>
	<p>L’originalité de cette deuxième édition réside par ailleurs d’abord dans l’ajout d’un volet portant sur le poids économique respectif de chacun des systèmes juridiques (PIB). Elle réside également dans la mise en relief, par le choix de quelques exemples significatifs, des efforts déployés par les États, les organisations internationales et la communauté marchande internationale pour élaborer un droit commercial international uniforme, en vue d’éviter les problèmes de conflits trans-systémiques de lois et de juridictions, afin de faciliter les échanges transfrontaliers dans le contexte de la mondialisation. Une dernière originalité de cette nouvelle édition consiste enfin, dans ce même contexte, à indiquer les langues officielles des entités politiques et à en montrer la répartition dans le monde contemporain selon chaque système juridique.</p>
	<p>Cette deuxième édition a été réalisée par M. Jabeur Fathally (D.E.S.D. Tunis, LL.L, LL.M., Ph.D. Ottawa), professeur adjoint à l'Université d'Ottawa et par M. Nicola Mariani (LL.L/LL.B. Ottawa), avocat international, sous la direction du professeur émérite Louis Perret, ancien doyen de la Faculté de droit d’Ottawa (section de droit civil) et du professeur émérite Alain-François Bisson, de cette même Faculté. Les recherches statistiques, les cartes et les graphiques ont été réalisés par Mme Ghislaine Pelletier (B.Arch. Montréal). Les graphiques et les cartes ont été actualisés par Mme Annie N’Tula Mankeni. Mme Sandrine Bidegorry a révisé les statistiques. La mise au point des  bibliographies a été effectuée par Mme Diana-Lynn Swan. Les traductions en anglais, en espagnol et en arabe sont l’œuvre  des divers membres de l’équipe de JuriGlobe.  Celles en chinois et en russe sont dues respectivement à M. Xiaodong Liang ( LL.B. Chendung; LL.M. Laval ) et à Mme Natalia Shpilkovskaya (LL.B. Moscou; LL.M. Ottawa).  La transposition des fichiers sur la toile a été accomplie par les webmestres de la Faculté de droit, MM.  Maxime Brasseur et Eric Proulx.</p>
