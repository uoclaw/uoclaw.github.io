	<h2>Systèmes de droit coutumier et systèmes mixtes  avec tradition de droit coutumier</h2>
	<p>Il n'existe plus guère aujourd'hui d'entités politiques dont le  système puisse être dit proprement et entièrement coutumier. La coutume peut  prendre les visages les plus divers, selon qu'elle est enracinée dans une  sagesse bâtie sur le concret quotidien ou, plus intellectuellement, dans des  grandes traditions spirituelles ou philosophiques. Néanmoins, le droit  coutumier (en tant que système, et pas seulement en tant que complément  accessoire du droit positif) joue encore un rôle, parfois d'une grande  importance, notamment en matière de statut personnel, dans un nombre  relativement élevé d'entités politiques de droit mixte. Le cas est évident pour  un certain nombre de pays africains. Mais cela vaut aussi par exemple, dans des  conditions certes différentes, pour le droit de la Chine ou de l'Inde.</p>
