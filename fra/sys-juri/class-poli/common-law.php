	<h2>Systèmes de common law et systèmes mixtes avec  tradition de common law</h2>
	<p>Comme le système de droit civil, le système de common law est vécu  de façons culturellement diverses dans le monde. Malgré les nuances parfois  considérables que peut entraîner cette diversité, et qui peuvent être encore  amplifiées par les circonstances politiques, on trouvera dans cette catégorie  les entités politiques dont le droit repose techniquement, pour l'essentiel,  sur les concepts et les modes d'organisation juridique de la common law  britannique, qui accorde une place primordiale à la jurisprudence, et non à la  loi, comme moyen ordinaire d'expression du droit commun. En conséquence, ont  été maintenues dans cette catégorie des entités politiques qui, rattachées de plus ou moins près à la tradition  britannique, possèdent parfois abondance de codes, de lois et d'instruments  normatifs non jurisprudentiels, mais où la common law jurisprudentielle  conserve son caractère de droit fondamental (tel est le cas, par exemple, aux  États-Unis, de la   Californie).</p>
