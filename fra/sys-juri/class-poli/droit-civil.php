	<h2>Systèmes de droit civil et systèmes mixtes avec tradition civiliste</h2>
	<p>On trouvera dans cette catégorie les entités politiques qui, à  côté d'autres sources, ont largement puisé dans l'héritage juridique romain et  qui, donnant préséance au droit écrit, ont résolument opté pour une  codification systématique de leur droit commun. Mais on y trouvera aussi des  entités politiques, généralement de droit mixte, qui, sans avoir recouru à la  technique de la loi codificatrice, ont retenu à des degrés divers suffisamment  d'éléments des constructions juridiques romaines, à titre de raison écrite,  pour être considérées comme affiliées à la tradition civiliste (tel est le cas,  par exemple de l'Écosse). D'un autre côté, on trouvera également dans cette  catégorie des entités politiques où l'influence romaine a été plus faible, mais  dont le droit, codifié ou non, repose sur une conception du rôle du droit  légiféré proche, à bien des égards, de celle des pays de tradition civiliste  «&nbsp;pure&nbsp;» (tel est le cas, par exemple, des pays de tradition  scandinave qui occupent, au sein de la famille de droit civil, quelquefois  appelée aussi &quot; romano-germanique &quot;, une position originale).</p>
