	<h2>Remarques introductives</h2>
	<p> Dans la réalisation de ce site sur les systèmes juridiques dans le monde, cinq catégories ont été retenues : droit civil, common law, droit coutumier, droit musulman, droit mixte, cette dernière catégorie ne renvoyant pas à un système, mais à une combinaison de systèmes. Cette catégorisation et la classification, évidemment imparfaite, des entités politiques qui en résulte appellent quelques remarques préliminaires.</p>
	<p> L'expression « entités politiques » comprend les États membres des Nations Unies, ainsi que les rares territoires indépendants qui n’en font pas partie, mais aussi parfois des divisions politiques de ces États. Il a en effet paru utile d'identifier les systèmes d'un certain nombre de territoires non indépendants (qui peuvent au demeurant jouir, dans certains cas, d'une autonomie interne plus ou moins forte), soit que leur situation géographique ne rend pas évidents leurs liens avec le système juridique de pays éloignés (c'est le cas par exemple des territoires français du Pacifique, de l'Océan indien ou des Antilles), soit que faisant partie d'ensembles politiques de type fédéral ou autre, leur système a acquis ou conservé, à l'intérieur de ces ensembles, des caractéristiques distinctes (c'est le cas par exemple du Québec, entité de droit mixte, dont le droit commun dans d'importants domaines, spécialement mais non exclusivement de droit privé, relève essentiellement du droit civil codifié, tandis que le droit commun des autres provinces et territoires du Canada est essentiellement fondé sur la common law). On notera que la prise en considération séparée de ces territoires non indépendants a eu parfois pour conséquence d’obliger à ventiler certains chiffres de population nationale ou de grossir l’importance apparente du nombre de pays relevant de l’un ou l’autre des systèmes. Les inconvénients qui pourraient en résulter pour une bonne interprétation des différents tableaux sont diminués par le fait qu’il est clairement fait mention, après le nom de ces territoires, provinces ou États, de celui de l’État central auquel ils sont rattachés.</p>
	<p> En ce qui concerne les droits religieux, nous n'avons retenu que le droit musulman, en raison de sa permanence et de sa grande extension. Le droit juif est cependant signalé une fois pour tenir compte des particularités du système de droit mixte israélien. D'autres systèmes de droit religieux à l'origine ont aujourd'hui perdu leur caractère et leur statut distinct en raison de la plus ou moins grande absorption d'un nombre plus ou moins grand de leurs éléments dans les systèmes coutumiers ou étatiques.</p>
	<p>Il convient de noter en outre que le droit canonique n’est pas un droit religieux&nbsp;: il s’agit d’un droit qui, bien qu’informé par des dogmes religieux, est d’origine humaine et appartient sans conteste à la famille de droit civil. </p>
	<p> Les droits coutumiers, dont la catégorie recouvre aussi celle des droits appelés autochtones, n’ont pas été signalés dans les cas où, même s’il peut en être parfois tenu compte à certaines fins, ils ne constituent pas une caractéristique systémique du droit de l’entité politique considérée.</p>
	<p> À la réflexion, nous avons écarté la catégorie des droits socialistes, naguère incontournable dans certaines classifications. Il est vrai que le marxisme-léninisme, malgré des bouleversements politiques récents, exerce une influence parfois encore importante sur l'organisation juridique de quelques pays. Mais le critère qui avait présidé à la création de la catégorie des droits socialistes pour les opposer aux droits occidentaux, était un critère matériel, alors que, sans nous en tenir à un critère superficiellement formel, nous avons plutôt privilégié en gros les aspects méthodologiques et techniques des systèmes, les concepts juridiques et les modes d'élaboration et d'expression du droit. </p>
    <p> On notera, par ailleurs, que dans le chapitre intitulé « Classification des systèmes juridiques et entités politiques correspondantes », les rubriques présentent les systèmes (de droit civil, de common law, de droit musulman, de droit coutumier et évidemment mixtes) au pluriel et non au singulier. Nous avons ainsi voulu tenir compte du fait que chaque système juridique (comme catégorie de classification) tend à acquérir des caractéristiques particulières selon les territoires et les populations auxquels il s'applique. Est-il à cet égard besoin de rappeler que malgré leur appartenance à une même famille juridique, il existe des différences, parfois substantielles, entre les droits positifs des États-Unis, du Royaume-Uni et de l'Australie par exemple (pays de tradition de common law) et qu'il n'en existe pas moins entre les droits positifs de la France, de l'Allemagne et du Chili par exemple (pays de tradition civiliste)? </p>
    <p> Il ne paraît pas inutile, pour terminer, d'insister à nouveau sur les modestes objectifs d'information que nous avons poursuivis. C'est pourquoi nous nous en sommes tenus à des catégories aisément identifiables par la communauté juridique mondiale.  On voudra  bien ne pas voir pour autant, dans notre entreprise, un désaveu d'efforts plus scientifiques ou plus raffinés de classification (ceux de Constantinesco, de Zweigert et Kötz, de Timsit, de Glenn par exemple), mais qui n'auraient pas mieux répondu au but immédiat que nous nous sommes proposé.</p>
