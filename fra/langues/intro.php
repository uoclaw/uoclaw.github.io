	<h2>Remarques introductives</h2>
	<p> Par « langues », on entend dans ce site seulement les langues officielles. Cependant, « la définition de ce qu’est une langue officielle n’est pas d’une parfaite netteté » (Michel Malherbe).</p>
	<p> Certaines langues officielles le sont <em>de jure</em>, en ce sens qu’elles font l’objet d’une reconnaissance expresse, constitutionnelle ou législative, sous des appellations qui peuvent d’ailleurs varier (le plus souvent « langue officielle », mais aussi quelquefois « langue nationale », « langue d’État », « langue de la République », etc.) ou d’une reconnaissance qui se déduit sans équivoque de textes imposant ou permettant leur utilisation à diverses fins institutionnelles. Il convient de remarquer que le caractère officiel de ces langues n’empêche pas qu’elles puissent être parfois ce qu’on peut appeler, sans connotation péjorative, des langues à statut restreint, soit que leur reconnaissance se limite à certaines parties du territoire, soit qu’elle se limite à certains domaines d’activité (fréquemment, éducation, protection culturelle ou communications de l’administration avec les citoyens), soit enfin que, dans les faits, elle soit de nature plutôt symbolique malgré son apparente grande étendue.</p>
	<p> Certaines langues officielles le sont, dit-on parfois, <em>de facto</em>, en ce sens que leur statut, non reconnu dans des textes spécifiques, résulte alors de leur emploi constant, parfois séculaire, dans tous les domaines majeurs de l’activité étatique (un exemple notable est celui de l’anglais aux États-Unis d’Amérique, tant au niveau fédéral que dans une minorité importante des États fédérés). Mais il existe aussi des langues coofficielles <em>de facto</em> à statut plus ou moins restreint.</p>
	<p> Sous le bénéfice de ces qualifications, comme il s’agit, dans ce site, de dresser la liste des langues officielles en rapport significatif avec le <em>système juridique </em><em>global </em>de chaque entité politique, nous n’avons retenu comme langues officielles (une centaine au total) que celles dont il est fait un emploi réellement substantiel et généralisé dans la législation et l’administration de la justice et nous avons écarté les langues officielles à statut matériellement ou territorialement restreint. L’existence, dans une entité politique, d’une ou, quelquefois, de plusieurs langues officielles à statut restreint est cependant signalé par un signe plus (+).</p>
	<p> Dans l’établissement de ce site, les auteurs ont pu se fonder, dans un nombre important de cas, sur leur connaissance personnelle des entités politiques considérées, mais ils ont dû faire appel, dans un nombre encore plus important de cas, à l’analyse, à l’interprétation et au recoupement de sources diverses. L’appréciation à distance et par documentations et études interposées, si soignées que soient celles-ci, des pratiques réelles en matière de langue des institutions juridiques laisse place à des doutes, peut-être à des erreurs. C’est pourquoi nous accueillerons avec reconnaissance toute observation nous permettant d’assurer la meilleure exactitude des informations fournies. </p>
