	<h2>	  &Agrave; propos de JuriGlobe </h2>
	<p> JuriGlobe est un groupe de recherche créé par des professeurs de la Faculté de droit de l’Université d’Ottawa pour développer une banque de données informatiques, plurilingue, accessible sur internet, sur les systèmes juridiques dans le monde, les langues officielles utilisées et un certain nombre de  conventions et autres instruments commerciaux multilatéraux. </p>
	<p> Un premier but est de fournir aux juristes et aux gens d’affaires un instrument simple et moderne leur permettant d’avoir une première indication du système juridique en vigueur et des langues officielles utilisées dans les divers pays où ils sont susceptibles de se rendre ou de mener leurs entreprises. </p>
	<p> Un deuxième objectif est de fournir un outil pédagogique visuel destiné à illustrer et à favoriser le développement de l’enseignement du droit comparé et du droit international dans le contexte de la formation des ressources humaines rendue nécessaire par la mondialisation des échanges. </p>
	<p> Un troisième objectif est de susciter une réflexion sur la bonne gouvernance de cette mondialisation en vue de l’élaboration d’instruments commerciaux multilatéraux animés d’un souci de sécurité juridique, facilement  accessibles et qui tiennent compte de la diversité des cultures juridiques, de leurs langues et de leur importance économique et démographique. </p>
