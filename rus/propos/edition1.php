	<h2>Предисловие к 1-ой версии (1999 г.) </h2>
	<p align="justify" ID="p-indent">Первоначальная идея создания  сайта «Правовые системы мира» возникла из особого интереса к данной теме и того  факта, что на Факультете права Оттавского университета преподается как  континентальное, так и общее право. Факультет права предлагает своим студентам  возможность четырехлетнего обучения по <a href="http://www.droitcivil.uottawa.ca/fr/programmes-d-etudes/programme-national/le-programme-national/id-menu-291.html?pid=291">национальной программе</a>, по результатам  выполнения которой они получают диплом по двум правовым системам, что улучшает их  карьерные перспективы в будущем. Таким образом возникло желание продемонстрировать  с помощью данного сайта истинную важность обеих правовых систем на международном  уровне. </p><p align="justify" ID="p-indent">
	  В работе над созданием сайта мы  также руководствовались интересом более общего характера. Действительно, после  50 лет либерализации мировой торговли, явившейся результатом деятельности ГАТТ  и ВТО, в промышленно развитых странах тарифы сократились с 50 до 3 процентов,  что способствовало тринадцатикратному увеличению объема торговли во всем мире. Например,  сегодня в Канаде 43 процента экономической деятельности  непосредственно связано с экспортом по сравнению с 18 процентами двадцать лет  назад. В настоящее время одно рабочее место из трех зависит от экспорта, а  каждый дополнительный миллиард долларов от экспорта создает 6 000–8 000 рабочих  мест в стране. </p><p align="justify" ID="p-indent">
	  В этой  связи мы подумали, что будет полезно предоставить юристам и бизнесменам  современный инструмент, содержащий информацию о правовых системах в различных  странах, в которых они могли бы вести коммерческую деятельность и заключать  международные соглашения.</p><p align="justify" ID="p-indent">
	  Конечно, при создании данного  инструмента мы столкнулись с достаточно сложной задачей, например, определение  критериев классификации различных правовых систем в большом количестве  смешанных систем и установление статуса закона в тех странах, в  которых проходят преобразования, в частности,  в Восточной Европе, Азии и Африке. Однако функционировать без такого гида в  условиях глобализации становится все труднее. Таким образом, полностью  осознавая существование подводных камней, мы тем не менее решили принять этот  вызов. Мы надеемся, что результаты нашей работы достигнут поставленной цели.  Учитывая, что со временем мы планируем усовершенствовать данный инструмент, мы  создали  отдельную страницу «форум для  обсуждения» для того, чтобы получить комментарии, критику и предложения  пользователей нашего сайта. </p><p align="justify" ID="p-indent">
	  Данный сайт создан на Факультете  права Оттавского университета г-ном Никола Мариани (бакалавр права, Оттавский  университет) и г-жой Грасиэлой Фунтес (магистр права, Университет МакГилла; доктор  права, Оттавский университет) под руководством профессора Алана Франсуа Биссона  и декана Луиса Перетта. Мы хотим выразить нашу благодарность за помощь г-же Наталии  Лалонд (Канадская библиотека Верховного суда), г-же Мари-Клод Лавигн, нашего компьютерного  консультанта, г-же Сильви Лебланк и г-ну Сержу Бруссо за техническую поддержку,  г-же Диане-Лин Сван (Библиотека права Оттавского университета), а также  переводчикам г-ну Кену Лароз и г-же Гэйл Лароз за их терпение и профессионализм.</p>
