	<h2>ОБЩАЯ БИБЛИОГРАФИЯ </h2>
	<p id="p-indent" align="justify">Данная библиография не  претендует на всеобщий охват. Тем не менее перечисленные работы дают  возможность получить информацию о концепциях правовых систем, методах их классификации,  классификациях непосредственно, а также об интересе, который они представляют,  и o спорах, которые они не перестают вызывать. </p>
