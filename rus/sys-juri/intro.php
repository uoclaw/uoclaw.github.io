	<h2>Вступительные замечания </h2>
	<p align="justify" id="p-indent">Для создания данного сайта были выбраны пять  категорий правовых систем: континентальное право, общее право, обычное право,  мусульманское право и смешанные системы, которые содержат в себе элементы  нескольких правовых систем. Использование такой категоризации и классификации  политических образований требуют нескольких предварительных пояснений. </p>
	<p align="justify" id="p-indent"> Термин «политическое образование» относится как к  стране-члену ООН, так и к нескольким независимым территориям, которые не  являются членами этой организации. Данный термин используется также в отношении  внутренних политических подразделений стран. Действительно, важно  идентифицировать правовые системы ряда зависимых территорий, отдельные из  которых имеют различный уровень автономии. Географическое расположение таких  территорий не всегда указывает на существование связи с правовыми системами  отдаленных стран (например, французские территории в Тихом океане, Индийском  океане или Вест-Индии). Кроме того, зависимая территория может являться  составной частью федеральной или другой политической структуры, хотя ее  правовая система может обладать особыми характеристиками (например, Квебек  является смешанной правовой системой, в которой право, по крайней мере в  отдельных важных областях частного права, основано на кодифицированном  континентальном праве, тогда как на остальной территории Канады применяется  общее право). Следует также отметить, что рассмотрение зависимых территорий повлекло  за собой необходимость отдельного указания демографических данных таких  территорий, а также увеличение количества стран, относящихся к определенной  правовой системе. Однако неточности, которые могли возникнуть и препятствовать  правильному пониманию различных диаграмм, были устранены с помощью четкого  указания стран, к которым относятся зависимые территории, провинции и штаты. </p>
	<p align="justify" id="p-indent"> Что касается религиозных правовых систем, то мы  включили только мусульманское право из-за его постоянной, широко основанной  природы. Мы обратились также к иудейскому праву для того, чтобы отразить особенности  смешанной правовой системы Израиля. Следует отметить, что некоторые  первоначально религиозные правовые системы утратили стой характер и  отличительный статус вследствие поглощения ряда их элементов обычным правом и  другими правовыми системами. </p>
	<p align="justify" id="p-indent"> Обычное право, которое включает в себя право  коренного населения, не указывается в тех случаях, когда оно не является  ключевой характеристикой соответствующей правовой системы политического  образования. </p>
	<p align="justify" id="p-indent"> По результатам обсуждений мы исключили категорию  «социалистического права», хотя в прошлом включение этой категории в отдельные  классификации было неизбежно. В действительности, несмотря на недавние  политические перевороты, марксизм-ленинизм продолжает играть существенную роль  в отдельных странах. Но критерий, положенный в основу категории  «социалистического права» в противоположность западному праву, является  материальным, тогда как мы придавали гораздо большую важность методологическим  и техническим аспектам правовых систем, правовым концепциям и методологиям  развития и выражения права, не ограничивая себя поверхностными формальными  критериями.  </p>
	<p align="justify" id="p-indent"> Кроме того, Вы заметите, что заголовки в главе  «Классификация правовых систем и соответствующих политических образований» обращаются  к правовым системам во множественном числе (например, системы континентального  права, системы общего права, системы мусульманского права, системы обычного  права и смешанные системы). Таким образом мы хотели пояснить тот факт, что  каждая правовая система (как категория классификации) имеет тенденцию  приобретать специфические особенности населения и территорий, где используется  правовая система. Следует отметить, что несмотря на отношение к одной правовой  системе, есть существенные различия между законами Соединенных Штатов Америки,  Великобритании и Австралии (страны общего права) и между законами Франции,  Германии и Чили (страны континентального права).</p>
	<p align="justify" id="p-indent">В заключение стоит еще раз подчеркнуть скромность наших целей в  представлении собранной информации. Мы ограничились теми категориями, которые  являются легко узнаваемыми в международном юридическом сообществе. Такая  классификация способствовала достижению поставленных перед нами целей. Однако  наши усилия по подготовке классификаций не должны рассматриваться как отказ от более  научных и изысканных классификаций, предложенных Constantinesco, Zweigert и Koltz, Timsit и Glenn.</p>
