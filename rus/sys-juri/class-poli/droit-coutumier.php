	<h2>СИСТЕМЫ ОБЫЧНОГО ПРАВА И СМЕШАННЫЕ СИСТЕМЫ С ТРАДИЦИЯМИ ОБЫЧНОГО ПРАВА </h2>
	<p align="justify" id="p-indent">
	  Едва ли какие-либо страны или политические образования в сегодняшнем мире функционируют в правовой системе, о которой можно  сказать, что она типично и полностью основана на обычаe. Обычай может принимать различные облики в зависимости от того, основан ли он на мудрости реального ежедневного опыта или на более интеллектуальной базе великих духовных или философских традиций. Как бы то ни было, обычное право как система, а не только как дополнение к действующему праву, иногда все еще продолжает играть существенную роль, например, в вопросах статуса личности, в относительно большом количестве политических образований со смешанными правовыми системами. Это однозначно относится к ряду африканских стран, а также характерно, например, для права Китая или Индии, хотя при совсем других обстоятельствах.</p>
	
