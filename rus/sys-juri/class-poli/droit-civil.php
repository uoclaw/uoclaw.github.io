	<h2>СИСТЕМЫ КОНТИНЕНТАЛЬНОГО ПРАВА И СМЕШАННЫЕ  СИСТЕМЫ С ТРАДИЦИЯМИ КОНТИНЕНТАЛЬНОГО ПРАВА </h2>
	<p align="justify" id="p-indent">В данной категории Вы сможете найти политические  образования, которые, не считая других источников, получили свое развитие в  значительной степени из наследия римского права и которые ввиду верховенства  письменного права твердо остановили свой выбор на систематической кодификации  своего законодательства. Тем не менее, здесь Вы также сможете найти политические  образования, в основном со смешанными правовыми системами, которые хотя и не  обратились к технике кодификации, но в различной степени сохранили достаточное  количество элементов римского права в письменной форме, что позволяет отнести  данные образования к традициям континентального права (например, Шотландия). С  другой стороны, данная категория включает в себя политические образования,  которые подверглись меньшему влиянию римского права, но чье законодательство,  кодифицированное или нет, основано на осознании роли права, выраженного в  законах, которое во многих отношениях отражает подход стран с традициями  “чистого” континентального права (например, скандинавские страны, которые  занимают свое исходное положение в “романо-германской” семье).	</p>
