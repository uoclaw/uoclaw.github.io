	<h2>СИСТЕМЫ ОБЩЕГО ПРАВА И СМЕШАННЫЕ СИСТЕМЫ С ТРАДИЦИЯМИ ОБЩЕГО ПРАВА </h2>
	<p align="justify" id="p-indent">
	  Как и система континентального права, система общего права впитала в себя разнообразие культурных форм всего мира. Несмотря на существенные нюансы, которые такое разнообразие иногда может создать и которые проявляются еще сильнее благодаря политическим обстоятельствам, данная категория включает в себя политические образования, право которых в основном технически основано на концепциях английского общего права и правовых организационных методах, предоставляющих приоритет прецедентному праву как первоначальному средству выражения общего закона. Таким образом, данная категория включает в себя страны и политические образования, которые не всегда могут иметь близкие связи с английской традицией и которые иногда обладают множеством кодексов, законов и неправовых нормативных актов, но для которых юриспруденция общего права остается их характерной чертой в качестве основного права (например, Калифорния, США). </p>
