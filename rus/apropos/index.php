	<h2><strong>«JuriGlobe»</strong></h2>
	<p id="p-indent" align="justify">«JuriGlobe» – исследовательская  группа, сформированная профессорами Факультета права Оттавского университета.  Усилия этой группы направлены на развитие многоязычного информационного банка  данных, доступного для всех в Интернете и содержащего общую информацию,  относящуюся к различным правовым системам, некоторым наиболее важным  международным торговым соглашениям, а также к другим многосторонним торговым  инструментам.  </p>
	<p align="justify" id="p-indent">Первая цель проекта состоит в том, чтобы  предоставить юристам и бизнесменам простую и современную схему, которая  позволит им получить первоначальное представление о правовых системах и  официальных языках, используемых в тех странах, которые они посетят или в  которых они, возможно, будут работать. </p>
	<p align="justify" id="p-indent">Вторая задача проекта – предоставить визуальный  образовательный инструмент, нацеленный на стимулирование обучения сравнительному  и международному праву и подготовку студентов к работе в глобализирующемся  мире.  </p>
	<p align="justify" id="p-indent">Третья и заключительная задача – стимулировать  обсуждение о надлежащем управлении глобализацией для того, чтобы развивать  динамичные многосторонние международные торговые инструменты, которые и  доступны, и основаны на признанных принципах, и принимают во внимание  многообразие различных правовых систем, их языки и экономическую и  демографическую значимость.</p>
